/// Replace each `{}` marker in `template` with the next arg, in order.
fn fill(template: &str, args: &[&str]) -> String {
    let mut out = String::with_capacity(template.len());
    let mut parts = template.split("{}");
    if let Some(first) = parts.next() {
        out.push_str(first);
    }
    for (arg, part) in args.iter().zip(parts) {
        out.push_str(arg);
        out.push_str(part);
    }
    out
}

impl Strings {
    /// Section header for a multi-disc album, e.g. "Disc 2".
    pub fn disc(&self, disc: u32) -> String {
        fill(&self.disc_t, &[&disc.to_string()])
    }

    /// Sample-rate / bit-depth line, e.g. "44.1 kHz · 24-bit". `khz` is already
    /// formatted by the caller (integer vs one-decimal).
    pub fn audio_spec(&self, khz: &str, bit_depth: u32) -> String {
        fill(&self.audio_spec_t, &[khz, &bit_depth.to_string()])
    }

    /// Bitrate line, e.g. "320 kbps".
    pub fn bitrate(&self, kbps: u32) -> String {
        fill(&self.bitrate_t, &[&kbps.to_string()])
    }

    /// Track-count label, e.g. "1 track" / "12 tracks". Two-form plural
    /// (singular when `n == 1`); languages with richer plural rules fall back
    /// to the general form.
    pub fn n_tracks(&self, n: i64) -> String {
        let template = if n == 1 {
            &self.n_tracks_one
        } else {
            &self.n_tracks_other
        };
        fill(template, &[&n.to_string()])
    }

    pub fn bp_system_volume(&self, value: &str) -> String {
        fill(&self.bp_system_volume_t, &[value])
    }

    pub fn bp_app_volume(&self, value: &str) -> String {
        fill(&self.bp_app_volume_t, &[value])
    }

    pub fn bp_sample_rate(&self, source_hz: u32, device_hz: u32) -> String {
        fill(
            &self.bp_sample_rate_t,
            &[&source_hz.to_string(), &device_hz.to_string()],
        )
    }

    pub fn bp_bit_depth(&self, source: u32) -> String {
        fill(&self.bp_bit_depth_t, &[&source.to_string()])
    }

    pub fn failed_exclusive(&self, err: &str) -> String {
        fill(&self.failed_exclusive_t, &[err])
    }

    pub fn failed_switch_device(&self, err: &str) -> String {
        fill(&self.failed_switch_device_t, &[err])
    }

    pub fn failed_save_settings(&self, err: &str) -> String {
        fill(&self.failed_save_settings_t, &[err])
    }
}

/// Build a `Strings` literal from `key: "value"` pairs. Expands to a full struct
/// literal, so a missing or misspelled key is a compile error.
#[macro_export]
macro_rules! lang {
    ($($key:ident : $val:literal),* $(,)?) => {
        $crate::i18n::Strings {
            $( $key: ::gpui::SharedString::new_static($val) ),*
        }
    };
}

macro_rules! languages {
    ($($variant:ident => ($code:literal, $name:literal, $table:path)),* $(,)?) => {
        /// A supported UI language.
        #[derive(Copy, Clone, PartialEq, Eq, Debug)]
        pub enum Lang {
            $($variant),*
        }

        impl Lang {
            /// All languages, in menu order.
            pub fn all() -> &'static [Lang] {
                &[$(Lang::$variant),*]
            }

            /// BCP-47-ish short code persisted in settings (e.g. "en", "zh").
            pub fn code(self) -> &'static str {
                match self { $(Lang::$variant => $code),* }
            }

            /// Endonym shown in the language picker (not itself localized).
            pub fn display_name(self) -> &'static str {
                match self { $(Lang::$variant => $name),* }
            }

            /// The string table for this language.
            pub fn strings(self) -> &'static Strings {
                match self { $(Lang::$variant => &$table),* }
            }

            /// Exact match on a stored code.
            pub fn from_code(code: &str) -> Option<Lang> {
                match code {
                    $($code => Some(Lang::$variant),)*
                    _ => None,
                }
            }
        }
    };
}

