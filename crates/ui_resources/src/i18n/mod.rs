//! Hardcoded, compile-time-checked localization tables.
//!
//! Design (see `doc.md`):
//! - [`Strings`] is the schema: one `SharedString` field per UI string. Built
//!   with the `const fn` `SharedString::new_static`, so every entry is the
//!   zero-allocation `Borrowed(&'static str)` variant and `.clone()` on the
//!   render hot path is free.
//! - Each language is one `pub static <CODE>: Strings = lang!{ ... };` in its
//!   own file. Because `lang!` expands to a full struct literal, the compiler
//!   errors if a language is missing or misspells any key (completeness on the
//!   key axis).
//! - [`Lang`] + its lookup are generated by `languages!`, so the language list
//!   lives in one place and `Lang::strings` is an exhaustive `match`
//!   (completeness on the language axis).
//! - Interpolated strings store a `{}`-placeholder template in a field and are
//!   read through the accessor methods on `impl Strings` (one place, shared
//!   across all languages).

